/** 口算·缩句练习：句子主干提取题库（新华网 / 求是网时政语体改写）
 *
 * 规则：
 * - shortened：推荐主干（去定、状、补；并列宾语须整组保留）
 * - alternates：也可接受的同等严谨写法（如专名简称），不得残留应删修饰语
 * - source：语体来源说明；均为练习改写句，非原文照录
 */

export type ShortenSentenceItem = {
  id: string
  difficulty: 'easy' | 'hard'
  sentence: string
  /** 推荐主干（标准答案） */
  shortened: string
  /** 也可接受（同判正确） */
  alternates?: string[]
  /** 来源说明 */
  source: string
}

function S(
  id: string,
  difficulty: ShortenSentenceItem['difficulty'],
  sentence: string,
  shortened: string,
  source: string,
  alternates?: string[],
): ShortenSentenceItem {
  return {
    id,
    difficulty,
    sentence,
    shortened,
    source,
    ...(alternates?.length ? { alternates } : {}),
  }
}

