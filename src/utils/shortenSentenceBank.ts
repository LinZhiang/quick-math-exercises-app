/** 口算·缩句练习：句子主干提取题库 */

export type ShortenSentenceItem = {
  id: string
  difficulty: 'easy' | 'hard'
  sentence: string
  /** 标准缩句答案（主干） */
  shortened: string
  /** 可接受的其他写法 */
  alternates?: string[]
  /** 来源说明 */
  source: string
}

function S(
  id: string,
  difficulty: ShortenSentenceItem['difficulty'],
  sentence: string,
  shortened: string,
  source: string,
  alternates?: string[],
): ShortenSentenceItem {
  return {
    id,
    difficulty,
    sentence,
    shortened,
    source,
    ...(alternates?.length ? { alternates } : {}),
  }
}

