export function getExtraQaByDifficulty(diff) {
  return EXTRA_QA[diff] || []
}

export function assertExtraData() {
  for (const diff of /** @type {const} */ (['easy', 'normal', 'hard'])) {
    const rows = EXTRA_QA[diff] || []
    const stems = new Set()
    for (let i = 0; i < rows.length; i++) {
      const row = rows[i]
      if (!Array.isArray(row) || row.length < 4) {
        throw new Error(`${diff}[${i}] 格式应为 [stem, correct, distractors[], topicTag]`)
      }
      const [stem, correct, distractors, topicTag] = row
      const st = String(stem || '').trim()
      const c = String(correct || '').trim()
      const tag = String(topicTag || '').trim()
      if (!st.endsWith('？')) throw new Error(`${diff}[${i}] 题干须以？结尾: ${st}`)
      if (!c) throw new Error(`${diff}[${i}] 缺正确答案`)
      if (!tag || !/^[a-z]+$/.test(tag)) throw new Error(`${diff}[${i}] topicTag 须为短 ascii: ${tag}`)
      if (!Array.isArray(distractors) || distractors.length < 4) {
        throw new Error(`${diff}[${i}] 干扰项至少 4 个: ${st}`)
      }
      const dSet = new Set(distractors.map((d) => String(d).trim()).filter(Boolean))
      if (dSet.size < 4) throw new Error(`${diff}[${i}] 干扰项须 ≥4 个互不相同: ${st}`)
      if (dSet.has(c)) throw new Error(`${diff}[${i}] 干扰项含正确答案: ${st}`)
      if (stems.has(st)) throw new Error(`${diff} 重复题干: ${st}`)
      stems.add(st)
    }
    if (stems.size < 200) {
      throw new Error(`${diff} 唯一题干不足 200（实际 ${stems.size}）`)
    }
  }
}

const isMain =
  typeof process !== 'undefined' &&
  process.argv[1] &&
  String(process.argv[1]).replace(/\\/g, '/').endsWith('/life-sense-extra-data.mjs')

if (isMain) {
  const counts = {
    easy: (EXTRA_QA.easy || []).length,
    normal: (EXTRA_QA.normal || []).length,
    hard: (EXTRA_QA.hard || []).length,
  }
  console.log('counts', counts, 'total', counts.easy + counts.normal + counts.hard)
  assertExtraData()
  console.log('assertExtraData: OK')
}
